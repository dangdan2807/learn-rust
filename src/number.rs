fn main() {
    // kiểu dữ liệu integer
    let a: i32 = 111_111; // decimal
    let b: i32 = 0xff; // hex
    let c: i32 = 0o77; // octal
    let d: i32 = 0b1111_0000; // binary
    let e: u8 = b'A'; //byte (u8 only)
    println!("a: {}", a);
    println!("b: {}", b);
    println!("c: {}", c);
    println!("d: {}", d);
    println!("e: {}", e);
}